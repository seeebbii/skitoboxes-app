class Address {
  final int? id;
  final String? name;
  final String? receiverNumber;
  final String? receiverName;
  final String? houseNumber;
  final String? street;
  final String? city;
  final String? state;
  final String? zipCode;

  Address({
    this.id,
    this.name,
    this.receiverNumber,
    this.receiverName,
    this.houseNumber,
    this.street,
    this.city,
    this.state,
    this.zipCode,
  });
}
